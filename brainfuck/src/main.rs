struct BFVM {
    memory : [u8; 300000],
    mem_pointer : usize,

    code : Vec<char>,
    code_pointer : usize,
}

impl BFVM {
    fn new(code : &str) -> Self {
        Self {
            memory : [0; 300000],
            mem_pointer : 0,
            code : code.chars().collect(),
            code_pointer : 0,
        }
    }
    
    fn run(&mut self) {
        loop {

            if self.code_pointer == self.code.len() {
                return;
            }

            match self.code[self.code_pointer] {
                '>' => self.mem_pointer += 1,
                '<' => self.mem_pointer -= 1,
                '+' => self.memory[self.mem_pointer] = self.memory[self.mem_pointer].overflowing_add(1).0,
                '-' => self.memory[self.mem_pointer] = self.memory[self.mem_pointer].overflowing_sub(1).0,
                '.' => print!("{}", self.memory[self.mem_pointer] as char),
                ',' => self.read_input(),
                _ => (),
            }

            match self.code[self.code_pointer] {
                '[' => self.jump_forward(),
                ']' => self.jump_backwards(),
                _ => self.code_pointer += 1,
            }
        }
    }

    fn jump_forward(&mut self) {
        if self.memory[self.mem_pointer] == 0 {
            let mut counter = 1;

            while counter != 0 {
                self.code_pointer += 1;

                match self.code[self.code_pointer] {
                    '[' => counter += 1,
                    ']' => counter -= 1,
                    _ => ()
                }
            }
        } else {
            self.code_pointer += 1;
        }
    }


    fn jump_backwards(&mut self) {
        if self.memory[self.mem_pointer] != 0 {
            let mut counter = 1;

            while counter != 0 {
                self.code_pointer -= 1;
                
                match self.code[self.code_pointer] {
                    '[' => counter -= 1,
                    ']' => counter += 1,
                    _ => ()
                }
            }
        } else {
            self.code_pointer += 1;
        }
    }

    fn read_input(&mut self) {
        let input: Option<u8> = std::io::stdin()
            .bytes() 
            .next()
            .and_then(|result| result.ok());
        
        self.memory[self.mem_pointer] = input.unwrap();
    }
}